 # Step 1: Store the information in variables  
name = "John Doe"         # String for name
hometown = "New York"     # String for hometown
age = 25                 # Integer for age

# Step 2: Store the information in a dictionary using {}
user_info = {
    "name": name,
    "hometown": hometown,
    "age": age
}

# Step 3: Print the values on separate lines using \n
print(f"Name: {user_info['name']}\nHometown: {user_info['hometown']}\nAge: {user_info['age']}") #using a single print() statement \n to print answers in different lines
