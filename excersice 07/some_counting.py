# 1. Loop that counts up from 0 to 50 in increments of 1
print("Counting up from 0 to 50 in increments of 1:")
for i in range(0, 51, 1): # Generates numbers from 0, ending before 51, and increasing by 1.
    print(i) # Prints the current value of i

print("\n" + "-"*40 + "\n")  # \n creates/adds a new line, "-"*40 Creates a line of 40 hyphens. this print function "print("\n" + "-"*40 + "\n")"m helps us to print a blank line then followed by 40 hypens then a blank line again which will help us seperate each set of out puts

